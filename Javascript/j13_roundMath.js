const random = Math.random();
console.log(`Random Created:  ${random}`);

console.log(`Between 0-10:  ${Math.floor(random * 10)}`);
console.log(`Between 0-100:  ${Math.floor(random * 100)}`);
console.log(`Between 0-10000:  ${Math.floor(random * 1000)}`);

console.log(`Between 0-10:  ${Math.ceil(random * 10)}`);
console.log(`Between 0-100:  ${Math.ceil(random * 100)}`);
console.log(`Between 0-10000:  ${Math.ceil(random * 1000)}`);

